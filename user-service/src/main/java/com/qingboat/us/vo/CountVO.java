package com.qingboat.us.vo;

public class CountVO {
    private Integer newsletterCount;
    private Integer pathwayCount;
    private Integer benefitCount;
    private Integer subscribeCount;
    private Integer keyCount;

    public Integer getNewsletterCount() {
        return newsletterCount;
    }

    public void setNewsletterCount(Integer newsletterCount) {
        this.newsletterCount = newsletterCount;
    }

    public Integer getPathwayCount() {
        return pathwayCount;
    }

    public void setPathwayCount(Integer pathwayCount) {
        this.pathwayCount = pathwayCount;
    }

    public Integer getBenefitCount() {
        return benefitCount;
    }

    public void setBenefitCount(Integer benefitCount) {
        this.benefitCount = benefitCount;
    }

    public Integer getSubscribeCount() {
        return subscribeCount;
    }

    public void setSubscribeCount(Integer subscribeCount) {
        this.subscribeCount = subscribeCount;
    }

    public Integer getKeyCount() {
        return keyCount;
    }

    public void setKeyCount(Integer keyCount) {
        this.keyCount = keyCount;
    }
}
